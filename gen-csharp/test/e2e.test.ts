Deno.test('e2e polymorphic - a multi-parent member fails its item loudly (one base record in C#)', () => {
  const multiParentDocument: OpenAPIV3.Document = {
    openapi: '3.0.0',
    info: { title: 'Multi-Parent API', version: '1.0.0' },
    paths: {},
    components: {
      schemas: {
        First: {
          oneOf: [
            { $ref: '#/components/schemas/Shared' },
            { $ref: '#/components/schemas/Only' }
          ],
          discriminator: { propertyName: 'kind' }
        },
        Second: {
          oneOf: [
            { $ref: '#/components/schemas/Shared' },
            { $ref: '#/components/schemas/Only' }
          ],
          discriminator: { propertyName: 'kind' }
        },
        Shared: {
          type: 'object',
          properties: { kind: { type: 'string' }, value: { type: 'string' } },
          required: ['kind']
        },
        Only: {
          type: 'object',
          properties: { kind: { type: 'string' } },
          required: ['kind']
        },
        Survivor: {
          type: 'object',
          properties: { ok: { type: 'string' } },
          required: ['ok']
        }
      }
    }
  }

  const { artifacts } = toArtifacts({
    traceId: 'gen-csharp-e2e',
    spanId: 'multi-parent',
    startAt: Date.now(),
    document: { type: 'oas', value: multiParentDocument },
    settings: { basePath: './src' },
    stackTrail: new StackTrail([]),
    silent: true,
    toGeneratorConfigMap: () => ({
      // @ts-expect-error - factory-emitted transform is monomorphic over Acc
      '@skmtc/gen-csharp': csharpEntry
    })
  })

  // Shared (claimed by First AND Second) errors loudly — a C# record
  // derives from one base record; Survivor still renders.
  assertEquals(artifacts['src/Acme/Api/Models/Shared.generated.cs'], undefined)
  assertEquals(
    artifacts['src/Acme/Api/Models/Survivor.generated.cs'] !== undefined,
    true
  )
})

