const csharpEntry = toCsharpEntry({ baseNamespace: 'Acme.Api.Models' })

const documentObject: OpenAPIV3.Document = {
  openapi: '3.0.0',
  info: { title: 'Fixture API', version: '1.0.0' },
  paths: {},
  components: {
    schemas: {
      User: {
        type: 'object',
        description: 'A user of the system',
        properties: {
          user_id: { type: 'string' },
          name: { type: 'string' },
          email: { type: 'string' },
          age: { type: 'integer', format: 'int64' },
          status: { $ref: '#/components/schemas/Status' },
          tags: { type: 'array', items: { type: 'string' } },
          address: {
            type: 'object',
            properties: {
              street: { type: 'string' },
              city: { type: 'string' }
            },
            required: ['street']
          },
          metadata: { type: 'object', additionalProperties: { type: 'string' } },
          list: { $ref: '#/components/schemas/UserList' },
          id: { $ref: '#/components/schemas/UserId' }
        },
        required: ['user_id', 'name']
      },
      Status: {
        type: 'string',
        enum: ['active', 'inactive', 'in-progress']
      },
      UserList: {
        type: 'array',
        items: { $ref: '#/components/schemas/User' }
      },
      UserId: { type: 'string', format: 'uuid' },
      Error: {
        type: 'object',
        properties: { error: { type: 'string' } },
        required: ['error']
      },
      Settings: {
        type: 'object',
        properties: { theme: { type: 'string' } },
        additionalProperties: true
      },
      Task: {
        type: 'object',
        properties: { name: { type: 'string' } },
        required: ['name']
      }
    }
  }
}

const runFixture = () => {
  return toArtifacts({
    traceId: 'gen-csharp-e2e',
    spanId: 'fixture',
    startAt: Date.now(),
    document: { type: 'oas', value: documentObject },
    settings: { basePath: './src' },
    stackTrail: new StackTrail([]),
    silent: true,
    toGeneratorConfigMap: () => ({
      // @ts-expect-error - factory-emitted transform is monomorphic over Acc
      '@skmtc/gen-csharp': csharpEntry
    })
  })
}

Deno.test('e2e - declarable schemas land one type per file; non-declarable refNames produce NO artifact (D6)', () => {
  const { artifacts, manifest } = runFixture()

  // UserList and UserId are non-declarable (array / scalar — C# has no
  // exported type alias) and are skipped-and-inlined; UserAddress is the
  // synthesized inline-object record in its OWN canonical file.
  assertEquals(Object.keys(artifacts).sort(), [
    'src/Acme/Api/Models/Error.generated.cs',
    'src/Acme/Api/Models/Settings.generated.cs',
    'src/Acme/Api/Models/Status.generated.cs',
    'src/Acme/Api/Models/Task.generated.cs',
    'src/Acme/Api/Models/User.generated.cs',
    'src/Acme/Api/Models/UserAddress.generated.cs'
  ])

  assertEquals(manifest.parseIssues.filter(issue => issue.level === 'error'), [])
})

