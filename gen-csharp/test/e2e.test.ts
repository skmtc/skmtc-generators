// --- Milestone CS-B: abstract-record polymorphic oneOf (D14) ------------

const polymorphicDocumentObject: OpenAPIV3.Document = {
  openapi: '3.0.0',
  info: { title: 'Polymorphic Fixture API', version: '1.0.0' },
  paths: {},
  components: {
    schemas: {
      Animal: {
        oneOf: [
          { $ref: '#/components/schemas/Dog' },
          { $ref: '#/components/schemas/Cat' }
        ],
        discriminator: {
          propertyName: 'petType',
          mapping: {
            dog: '#/components/schemas/Dog',
            cat: '#/components/schemas/Cat'
          }
        }
      },
      Dog: {
        type: 'object',
        properties: {
          petType: { type: 'string' },
          name: { type: 'string' },
          bark_volume: { type: 'integer', format: 'int64' }
        },
        required: ['petType', 'name']
      },
      Cat: {
        type: 'object',
        properties: {
          petType: { type: 'string' },
          name: { type: 'string' }
        },
        required: ['petType', 'name']
      },
      // A member whose ONLY property is the discriminator — legal in C#:
      // renders the bodyless `record Ghost : Spirit;` collapse.
      Spirit: {
        oneOf: [
          { $ref: '#/components/schemas/Ghost' },
          { $ref: '#/components/schemas/Wraith' }
        ],
        discriminator: { propertyName: 'petType' }
      },
      Ghost: {
        type: 'object',
        properties: { petType: { type: 'string' } },
        required: ['petType']
      },
      Wraith: {
        type: 'object',
        properties: {
          petType: { type: 'string' },
          name: { type: 'string' }
        },
        required: ['petType']
      },
      NonQualifying: {
        oneOf: [{ $ref: '#/components/schemas/Dog' }, { type: 'string' }]
      },
      Holder: {
        type: 'object',
        properties: { value: { $ref: '#/components/schemas/NonQualifying' } },
        required: ['value']
      }
    }
  }
}

